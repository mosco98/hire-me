export async function randomizeWord(arr: string[]) {
  const randomWord = arr[Math.floor(Math.random() * arr.length)];

  // Function to shuffle letters using Fisher-Yates shuffle algorithm
  function fisherYatesShuffle(word: string): string {
    if (word.length <= 1) return word;

    let shuffledWord = word;

    while (shuffledWord === word) {
      const array = word.split("");

      for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
      }
      shuffledWord = array.join("");
    }

    return shuffledWord;
  }

  const shuffledWord = fisherYatesShuffle(randomWord);

  return { word: randomWord, shuffledWord };
}

export function updateWordsArray(words: string[], newWord: string): string[] {
  words.pop();

  const nonEmptyWords = words.filter((w) => w !== "");

  if (nonEmptyWords.length > 0) {
    const lastWordIndex = words.lastIndexOf(
      nonEmptyWords[nonEmptyWords.length - 1]
    );
    words[lastWordIndex + 1] = newWord;
  } else {
    words[0] = newWord;
  }

  while (words.length < 15) {
    words.push("");
  }

  return words;
}
